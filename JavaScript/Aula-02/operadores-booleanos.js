// Operadores booleanos: comparações

// Igualdade
console.log(1 === 1); // true
console.log(1 === 2); // false

// Diferença
console.log(1 !== 1); // false
console.log(1 !== 2); // true

// Maior que
console.log(1 > 1); // false
console.log(1 > 2); // false
console.log(2 > 1); // true

// Maior ou igual a
console.log(1 >= 1); // true
console.log(1 >= 2); // false

// Menor que
console.log(1 < 1); // false
console.log(1 < 2); // true

// Menor ou igual a
console.log(1 <= 1); // true
console.log(1 <= 2); // true


// === é igualdade estrita
// == é igualdade solta
// !== é diferença estrita
// != é diferença solta
// > é maior que
// >= é maior ou igual a
// < é menor que
// <= é menor ou igual a
// && é E
// || é OU
// ! é NÃO


let idadePessoa1 = 18;
let idadePessoa2 = 20;
console.log(idadePessoa1 >= 18 && idadePessoa2 >= 18); // true
console.log(!(idadePessoa1 >= 18)); // false



